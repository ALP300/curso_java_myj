    private java.util.List<String> usuarios = new java.util.ArrayList<>();
    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnMostrarActionPerformed
        String nombre = JOptionPane.showInputDialog(this, "Ingrese el nombre del usuario:");
        if (nombre != null && !nombre.trim().isEmpty()) {
            usuarios.add(nombre.trim());
        }
        // Mostrar todos los usuarios en el Ã¡rea de texto
        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < usuarios.size(); i++) {
            sb.append((i + 1) + ". " + usuarios.get(i));
            if (i < usuarios.size() - 1) {
                sb.append("\n");
            }
        }
        txtA.setText(sb.toString());
    }// GEN-LAST:event_btnMostrarActionPerformed

